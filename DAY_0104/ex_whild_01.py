# ---------------------------------------------------------------------
# 타이머 프로그램 만들기 => 다운 카운팅 : 10 9 8 ...... 3 2 1
downCnt = 10
while downCnt >= 1:
    print(f'다운 카운팅 {downCnt}초')
    downCnt -= 1

# 타이머 프로그램 만들기 => 다운 카운팅 : 1 2 3 ...... 8 9 10
downCnt = 1
while downCnt <= 10:
    print(f'다운 카운팅 {downCnt}초')
    downCnt += 1

# ---------------------------------------------------------------------
# 동일한 문제룰 for ~ in 반복문 구현
# ---------------------------------------------------------------------

for downCnt in range(10, 0, -1):
    print(f'다운 카운팅 {downCnt}초')

print()

for downCnt in range(1, 11):
    print(f'다운 카운팅  {downCnt}초')

