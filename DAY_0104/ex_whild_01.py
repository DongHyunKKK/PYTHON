#       단, Top 5로 가장 좋아 하는 음식 5개만 입력 받아 주세요.
#       => 사용자의 Top 5 음식명 출력
# ---------------------------------------------------------------------

TEST = False  # 디버깅 할 때 쓴다.

if TEST:
    myFoods = []
    for cnt in range(5):
        food = input(f'{cnt+1}번째 좋아하는 음식명 입력 : ')
        myFoods.append(food)

    # 결과 출력 ==>
    print('당신은 ', end = '')
    for food in myFoods:
        print(food, end = ', ' if food != myFoods[-1] else ' ')
        print('음식을 좋아하는 군요!')

    print()

    strFoods = ''
    for cnt in range(5):
        food = input(f'{cnt + 1}번째 좋아하는 음식명 입력 : ')
        strFoods = strFoods + food
        strFoods = strFoods + (', ' if cnt != 4 else ' ')
    print(f'strFoods => {strFoods}')

    # 결과 출력 =>
    print(f'당신은 {strFoods} 음식을 좋아하는 군요!')

